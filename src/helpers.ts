type Join<K, P> = K extends string
  ? P extends string
    ? `${K}.${P}`
    : never
  : never;

export type Paths<T> = {
  [K in keyof T & string]: T[K] extends object ? K | Join<K, Paths<T[K]>> : K;
}[keyof T & string];

export type LeafPaths<T, Prev extends string = ''> = {
  [K in keyof T]: T[K] extends string
    ? `${Prev}${K & string}`
    : T[K] extends object
    ? LeafPaths<T[K], `${Prev}${K & string}.`>
    : never;
}[keyof T];

export type GetByPath<
  T,
  P extends string
> = P extends `${infer Key}.${infer Rest}`
  ? Key extends keyof T
    ? GetByPath<T[Key], Rest>
    : never
  : P extends keyof T
  ? T[P]
  : never;

export interface TranslationType {}

export type TranslationLoader = () =>
  | TranslationType
  | Promise<TranslationType>;

